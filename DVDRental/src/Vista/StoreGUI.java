 public void cargarStore(int addres_id){
        DefaultTableModel modelo;
        modelo = (DefaultTableModel) jtListado1.getModel();    
        ArrayList<Store> listado = new ArrayList();
        listado=ControladorStore.listadoStore(addres_id);
        limpiarListadoTabla();
        for(int i= 0; i < listado.size(); i++){
              modelo.addRow(new Object[]{
              listado.get(i).getStore_id(),
              listado.get(i).getManager_id(),
              listado.get(i).getAddres_id(),
              listado.get(i).getLast_update(),
              });
        }
    }
