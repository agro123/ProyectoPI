    private void btnRegistrar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrar1ActionPerformed
        if(jTextField3.getText().trim().equals(""))
        JOptionPane.showMessageDialog(this,"Ingrese el ID");
        else{
            if (jTextField5.getText().trim().equals("")){
                JOptionPane.showMessageDialog(this,"Agregue el id del manager");

            }
            else {
                Store store = new Store();
                store.setStore_id(Integer.parseInt(jTextField3.getText()));
                store.setManager_id(Integer.parseInt(jTextField5.getText()));
                store.setAddres_id(obteneridAddres());

                Date date = new Date();
                store.setLast_update(new Timestamp(date.getTime()));

                int tamaño;
                tamaño = ControladorStore.listadoStore(store.getAddres_id()).size();

                if(tamaño == 0){
                    int resultado = 0;
                    resultado = ControladorStore.grabarStore(store);
                    if(resultado == 1){
                        JOptionPane.showMessageDialog(this,
                            "Tienda Grabada con éxito",
                            "Confirmación",JOptionPane.INFORMATION_MESSAGE);
                    }
                    else{
                        JOptionPane.showMessageDialog(this,"Error al grabar",
                            "Confirmación",JOptionPane.ERROR_MESSAGE);
                    }

                    cargarStore(obteneridAddres());

                    btnNuevo.setText("Nuevo");
                    jTextField3.setEnabled(false);
                    jTextField5.setEnabled(false);

                    btnNuevo.setEnabled(true);
                    btnRegistrar.setEnabled(false);
                    btnBorrar.setEnabled(true);
                    btnModificar.setEnabled(true);
                    jtListado.setEnabled(true);
                    btnNuevo.requestFocusInWindow();

                }
                else{
                    JOptionPane.showMessageDialog(this,
                        "ID ya registrado","Confirmación",
                        JOptionPane.ERROR_MESSAGE);
                    jTextField3.requestFocusInWindow();
                }

            }
        }
    }//GEN-LAST:event_btnRegistrar1ActionPerformed

